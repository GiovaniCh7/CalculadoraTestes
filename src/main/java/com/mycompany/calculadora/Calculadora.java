
package com.mycompany.calculadora;

public class Calculadora {
     public float soma(int a, int b){
        return a + b;
     }
        public float subitracao(int a, int b){
        return a - b;
    }
        public float divisao(int a, int b){
        return a / b;
    }     
        public float Multiplicacao(int a, int b){
        return a * b;
    }
}

